// Verificar conexi√≥n a la base de datos (MEJORADO)
async function verificarConexion() {
  try {
    const connection = await db.getConnection();
    console.log("‚úÖ Conectado a la base de datos MySQL");
    
    // Verificar que las tablas principales existen
    const [tables] = await connection.query("SHOW TABLES");
    const tableNames = tables.map(row => Object.values(row)[0]);
    
    const tablesRequired = ['bienes', 'usuarios', 'ubicaciones', 'categorias', 'v_resumen_bienes'];
    const missingTables = tablesRequired.filter(table => !tableNames.includes(table));
    
    if (missingTables.length > 0) {
      console.warn("‚ö†Ô∏è  Tablas faltantes:", missingTables);
    } else {
      console.log("‚úÖ Todas las tablas necesarias est√°n presentes");
    }
    
    connection.release();
  } catch (err) {
    console.error("‚ùå Error de conexi√≥n a la base de datos:", err.message);
    console.error("üîß Verifica que MySQL est√© ejecut√°ndose en puerto", dbConfig.port);
    console.error("üí° Comando: docker ps | grep mysql");
    process.exit(1);
  }
}

