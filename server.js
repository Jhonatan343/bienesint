// Registrar un nuevo usuario
apiRouter.post("/usuarios", async (req, res) => {
  const {
    nombres,
    apellidos,
    telefono,
    correo_institucional,
    cedula,
    carrera,
  } = req.body;

  console.log("‚ûï Creando nuevo usuario:", { nombres, apellidos, cedula });

  // Validaciones mejoradas
  if (!nombres || !apellidos || !cedula) {
    return res.status(400).json({
      error: "Los campos nombres, apellidos y c√©dula son obligatorios",
    });
  }

  if (cedula.length < 10) {
    return res.status(400).json({
      error: "La c√©dula debe tener al menos 10 d√≠gitos",
    });
  }

  try {
    // Verificar que la c√©dula no exista
    const [existing] = await db.query(
      "SELECT id_usuario FROM usuarios WHERE cedula = ?",
      [cedula]
    );
    
    if (existing.length > 0) {
      console.log(`‚ùå C√©dula ya existe: ${cedula}`);
      return res.status(409).json({ error: "Ya existe un usuario con esta c√©dula" });
    }

    // Insertar
    const query = `
      INSERT INTO usuarios (nombres, apellidos, telefono, correo_institucional, cedula, carrera)
      VALUES (?, ?, ?, ?, ?, ?)
    `;
    const [result] = await db.query(query, [
      nombres.trim().toUpperCase(),
      apellidos.trim().toUpperCase(),
      telefono || null,
      correo_institucional?.toLowerCase() || null,
      cedula,
      carrera?.toUpperCase() || null,
    ]);

    const newUser = {
      id_usuario: result.insertId,
      nombres: nombres.trim().toUpperCase(),
      apellidos: apellidos.trim().toUpperCase(),
      telefono,
      correo_institucional: correo_institucional?.toLowerCase(),
      cedula,
      carrera: carrera?.toUpperCase(),
    };

    console.log(`‚úÖ Usuario creado exitosamente: ID ${result.insertId}`);
    res.status(201).json(newUser);
  } catch (err) {
    console.error("‚ùå Error al guardar el usuario:", err);
    if (err.code === "ER_DUP_ENTRY") {
      res.status(409).json({ error: "Ya existe un usuario con esta informaci√≥n" });
    } else {
      res.status(500).json({ error: "Error al guardar el usuario" });
    }
  }
});

